	void DrawNewCube::Bind() const
	{
        s_NewCubeData.NewCubeVertexArray = std::make_unique<VertexArray>();

        std::shared_ptr<VertexBuffer> CubeVertexBuffer;
        CubeVertexBuffer.reset(VertexBuffer::Create(NewCube_Vertices, sizeof(NewCube_Vertices)));

        CubeVertexBuffer->SetLayout(
            {
                { ShaderDataType::Float3, "a_Position" },
                { ShaderDataType::Float3, "a_Normal" },
                { ShaderDataType::Float2, "a_TexCoord" }
            }
        );

        s_NewCubeData.NewCubeVertexArray->AddVertexBuffer(CubeVertexBuffer);
	}
	void DrawNewCube::OnDraw(const std::shared_ptr<Shader>& shader) const
	{
        s_NewCubeData.NewCubeVertexArray->Bind();
        //Renderer::DrawIndexed(s_CubeData.CubeVertexArray);
        glDrawArrays(GL_TRIANGLES, 0, 36);
        s_NewCubeData.NewCubeVertexArray->Unbind();
	}
